def ftc(x): # Fahrenheit to Celsius
    return (x -32) * (5 / 9)

def ktc(x): # Kelvin to Celsius
    return x - 273.15

def ftk(x): # Fahrenheit to Kelvin
    return (x + 459.67) * (5 / 9)

def ctk(x): # Celsius to Kelvin
    return x + 273.15

def ktf(x): # Kelvin to Fahrenheit
    return x * (9 / 5) - 459.67

def ctf(x): # Celsius to Fahrenheit
    return x * (9 / 5) + 32

